function getRandomInt(min, max) {
    return Math.floor(Math.random() * (max - min + 1)) + min;
}

a = getRandomInt(-9,9);
b = getRandomInt(-9,9);

if (a >=0 && b >= 0) {
    alert(a-b);
} else if (a < 0 && b < 0) {
    alert(a*b);
} else if (a < 0 && b >= 0 || a >= 0 && b < 0) {
    alert(a+b);
}

